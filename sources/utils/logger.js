function info(message) {
	// eslint-disable-next-line no-console
	console.log(message);
}

function warn(message) {
	// eslint-disable-next-line no-console
	console.warn(message);
}

function error(message) {
	// eslint-disable-next-line no-console
	console.error(message);
}

export default {
	info,
	warn,
	error
};
